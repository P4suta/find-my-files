//! Contract emitter — radiates the contract to two committed artifacts
//! (ADR-0018):
//!   - `app/FindMyFiles/Engine/Generated/EngineContract.g.cs` — the C# side.
//!   - `docs/contract.md` — a language-neutral reference table rendered into
//!     the design-docs book (the FFI / pipe contract page).
//!
//! Every value is read from the compiled contract items themselves
//! (`offset_of!`/`size_of!` on the real `#[repr(C)]` types, the actual
//! consts), so the generated numbers cannot drift from the Rust truth — and
//! the two artifacts cannot disagree with each other. Both outputs are
//! committed; `tests/drift.rs` runs `--check` inside `cargo test --workspace`,
//! so an out-of-date generated file fails the ordinary test gate.
//!
//! Usage: `just contract-gen` (write) / `gen-contract --check` (verify).

use std::mem::{offset_of, size_of};
use std::path::PathBuf;

use fmf_contract::pod::{
    FmfBlob, FmfEvent, FmfPage, FmfQueryOptions, FmfRow, FmfVolumeStatus, FrameHeader,
};
use fmf_contract::{codes, counters, events, limits, opcodes, versions};

fn pascal(snake: &str) -> String {
    snake
        .split('_')
        .map(|w| {
            let mut c = w.chars();
            match c.next() {
                Some(f) => f.to_uppercase().collect::<String>() + c.as_str(),
                None => String::new(),
            }
        })
        .collect()
}

